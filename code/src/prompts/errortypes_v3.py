"""
Error Types v3 프롬프트 클래스
"""

from .base import BasePrompt


class ErrorTypesV3Prompt(BasePrompt):
    """
    Error Types v3 프롬프트
    오류 유형을 명시적 체크리스트 방식으로 제시하는 프롬프트
    """

    @property
    def name(self) -> str:
        """프롬프트 이름 반환"""
        return "errortypes_v3"

    def system_message(self) -> str:
        """시스템 메시지 반환 (사용하지 않음)"""
        return ""

    def format_user_message(self, text: str) -> str:
        """
        사용자 메시지 포맷팅

        Args:
            text: 교정할 원문 텍스트

        Returns:
            str: 포맷팅된 프롬프트
        """
        template = """# 지시
다음 체크리스트에 따라 원문의 오류를 검토하고 교정하세요.

## 필수 확인 사항 (체크리스트)
1. **띄어쓰기**: 단어 간 띄어쓰기 규칙 준수
2. **맞춤법**: 철자, 받침, 모음 등 정확한 표기
3. **문장부호**: 쉼표, 마침표, 따옴표 등 올바른 사용
4. **조사**: 주격/목적격/보조사 등 문맥에 맞는 조사 선택
5. **표준어**: 비표준어/방언을 표준어로 교정
6. **사이시옷**: 합성어에서 사이시옷 규칙 적용
7. **능동/피동**: 능동태와 피동태 올바른 사용
8. **문법**: 어미, 시제, 품사 활용 등 문법 규칙
9. **비문**: 문장 구조가 어색하거나 불완전한 경우 자연스럽게 재구성
10. **논리오류**: 문맥상 모순되거나 논리적으로 맞지 않는 표현 수정

## 교정 원칙
- 위 10가지 항목을 순서대로 점검하세요.
- 오류가 없는 항목은 건드리지 마세요.
- 오류가 있는 항목만 최소한으로 수정하세요.
- 설명 없이 교정된 문장만 출력하세요.

# 예시 1: 조사 오류
<원문>
그 모습은 정말 인상적이었다.
<교정>
그 모습이 정말 인상적이었다.

# 예시 2: 표준어
<원문>
동태찌개를 먹으면서 눈 깜빡할 사이에 다 먹었어.
<교정>
동태찌개를 먹으면서 눈 깜짝할 사이에 다 먹었어.

# 예시 3: 사이시옷
<원문>
빗물이 뒤머리를 타고 흘러내렸고, 나뭇가지가 부러졌다.
<교정>
빗물이 뒷머리를 타고 흘러내렸고, 나뭇가지가 부러졌다.

# 예시 4: 비문
<원문>
이 문제는 해결이 필요한 것은 명확하다.
<교정>
이 문제는 해결이 필요하다는 것이 명확하다.

# 교정할 문장
<원문>
{text}
<교정>"""

        return template.format(text=text)
